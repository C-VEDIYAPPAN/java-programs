public class Arithmetic_Assignment_Operators {
    public static void main(String[] args) {
        int a=123;
        System.out.println(a);
        a+=10;
        System.out.println(a);
        a-=10;//a=a-10
        System.out.println(a);
        a*=10;
        System.out.println(a);
        a/=10;
        System.out.println(a);
        a%=10;
        System.out.println(a);
    }
}
